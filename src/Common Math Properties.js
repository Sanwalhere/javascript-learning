//--------------------------------------------------|Common Math Properties & Methods|-----------------------------------------------

//--- Returns the absolute value of a number:
console.log(Math.abs(-4.7));
console.log(Math.abs(-5.1));
console.log(Math.abs(4.7));
console.log(Math.abs(5));
console.log(Math.abs(5));

//--- Rounded to the nearest or equal integer even negative or positive:
console.log(Math.round(-4.7));
console.log(Math.round(-5.1));
console.log(Math.round(4.7));
console.log(Math.round(4.3));
console.log(Math.round(-5));
console.log(Math.round(5));

//--- Round down the integer:
console.log(Math.floor(-4.7));
console.log(Math.floor(-5.1));
console.log(Math.floor(4.7));
console.log(Math.floor(4.3));
console.log(Math.floor(-5));
console.log(Math.floor(5));

//--- Round up the integer:
console.log(Math.ceil(-4.7));
console.log(Math.ceil(-5.1));
console.log(Math.ceil(4.7));
console.log(Math.ceil(4.3));
console.log(Math.ceil(-5));
console.log(Math.ceil(5));

//--- Return minimum number:
console.log(Math.min(3, 8));

//--- Return maximum number:
console.log(Math.max(3, 8));

//--- Return square root of number:
console.log(Math.sqrt(25));

//--- Return power of number:
console.log(Math.pow(2, 5));

//--- Return Pi value:
console.log(Math.PI);

//--- Return random number less than 1:
console.log(Math.random());
