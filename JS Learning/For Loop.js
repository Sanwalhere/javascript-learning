//--------------------------------------------------|For Loop Example 1|--------------------------------------------------------

for (let i = 0; i < 11; ++i) {
  console.log(i);
}

//--------------------------------------------------|For Loop Example 2|--------------------------------------------------------

for (let i = 0; i < 5; i++) {
  console.log("Hello World");
}

//--------------------------------------------------|For Loop with Continue and Break|------------------------------------

for (let i = 0; i < 10; i++) {
  if (i === 3) {
    continue;
  } else if (i === 7) {
    break;
  }
  console.log(i);
}
